#include <vector>

using std::vector;

/**
 * @brief Removes duplicate elements from the given <code>vector</code>
 * After the call, the following will be true:
 *  1) v' is a subsequence of the original v
 *  2) No element will appear in v' more then once
 *  3) Every element in v will appear in v'
 * @param v 
 */
void uniquify(vector<int>& v);
